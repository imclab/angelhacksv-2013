@implementation LCGazePoint
@synthesize precision = _precision;
@synthesize x = _x;
@synthesize y = _y;

- (NSString *)description{
    return [NSString stringWithFormat:@"@% x: %f y:%f", [super description], _x, _y];
}

@end
